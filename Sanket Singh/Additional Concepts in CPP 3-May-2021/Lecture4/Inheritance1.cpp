class Counter { // parent class
protected: // new visibility modifier, all the protected properties are accessible in the class and in the derives classes
	int count;

public:
	Counter() : count(0) {
		cout<<"Base class constructor 1 called\n";
	}
	Counter(int c) : count(c) {
		cout<<"Base class constructor 2 called\n";
	}

	int get_count() const {
		return this->count;
	}

	Counter operator ++() {
		return Counter(++this->count);
	}
};

class CountDn : public Counter { // child class
public:
	CountDn() {
		cout<<"Derived class constructor 1 called\n";
	}

	CountDn(int c, int v) {
		cout<<"Derived class constructor 2 called\n";
	}

	CountDn(int c) : Counter(c) {
		cout<<"Derived class constructor 3 called\n";
	}
	Counter operator --() {
		return Counter(--this->count);
	}
};


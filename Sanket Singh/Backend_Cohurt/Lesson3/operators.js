// Arithmetic operators

console.log(10+12);
console.log(10-12);
console.log(10*12);
console.log(12/5);
console.log(12%7);

// comparison operators
console.log(10 > 2);
console.log(10 < 2);
console.log(10 >= 2);
console.log(10 <= 2);
console.log(10 !== 2); // strictly not equal
console.log(10 === 12); // strictly equal

// logical operators
console.log(true && (10 === 12));
console.log(true || false);
console.log(!true);